    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        try {
            if (updateRequesteeText.getText().length() < 1) {
                JOptionPane.showMessageDialog(null, "Error: Please fill the required fields!");
            } else {
                Requestee oldRequestee = new Requestee(oldRequesteeName);
                Requestee newRequestee = new Requestee(updateRequesteeText.getText());
                int res = RequesteeController.updateRequestee(oldRequestee, newRequestee);
                if (res == 1) {
                    JOptionPane.showMessageDialog(null, "Updated Succesfully");
                    if (recieval != null) {
                        recieval.setRequesteeCombo(recieval.getRequesteeCombo());
                    }
                    updateRequesteeText.setText("");
                    setComboData();
                    updateBtn.setEnabled(false);
                } else {
                    JOptionPane.showMessageDialog(null, "Error!");
                }
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddRequestee.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error: To delete this requestee you need to delete all the other records having this requestee!");
        }
}//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        try {
            Requestee deletedRequestee = new Requestee(updateRequesteeText.getText());
            int res = RequesteeController.deleteRequestee(deletedRequestee);
            if (res == 1) {
                JOptionPane.showMessageDialog(null, "Deleted Succesfully");
                if (recieval != null) {
                    recieval.setRequesteeCombo(recieval.getRequesteeCombo());
                }
                setComboData();
            } else {
                JOptionPane.showMessageDialog(null, "Error!");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddRequestee.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error: To delete this requestee you need to delete all the other records having this requestee!");
        }
}//GEN-LAST:event_deleteBtnActionPerformed

    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        try {
            if (addRequesteeText.getText().length() < 1) {
                JOptionPane.showMessageDialog(null, "Error: Please fill the required fields!");
            } else {
                String newRequestee = addRequesteeText.getText();
                Requestee requestee = new Requestee(newRequestee);
                int res = RequesteeController.addRequestee(requestee);

                if (res == 1) {
                    JOptionPane.showMessageDialog(null, "Added Succesfully");
                    if (recieval != null) {
                        recieval.setRequesteeCombo(recieval.getRequesteeCombo());
                    }
                    addRequesteeText.setText("");
                    setComboData();

                } else {
                    JOptionPane.showMessageDialog(null, "Error!");
                }
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddRequestee.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddRequestee.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addBtnActionPerformed

